package Operators;

public class RelationalOperators_4 {
    public static void main(String[] args) {
//        <=, less than or equal to returns true if the left-hand side is less than or equal to the right-hand side.
        int a = 10;
        int b = 11;
        System.out.println(a <= b); //O/P: true
        System.out.println(a >= b); // O/P: false
        System.out.println(b >= a); // O/P: true

        int a1 = 11;
        int b1 = 11;
        System.out.println(a1 <= b1); //O/P: true
        System.out.println(a1 >= b1); // O/P: true
        System.out.println(b1 >= a1); // O/P: true

    }
}
