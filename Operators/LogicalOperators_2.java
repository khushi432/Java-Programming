package Operators;

public class LogicalOperators_2 {
    public static void main(String[] args) {
//        ||, Logical OR: returns true if at least one condition is true.

        boolean a = true; // 1
        boolean b = false; // 0
        System.out.println(a || b); // O/P: true

        boolean a1 = false; //
        boolean b1 = true; //
        System.out.println(a1 || b1); // O/P: true

        boolean a2 = false; //
        boolean b2 = false; //
        System.out.println(a2 || b2); // O/P: false

        boolean a3 = true; //
        boolean b3 = true; //
        System.out.println(a3 || b3); // O/P: true

    }
}
