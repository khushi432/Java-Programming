package Operators;

public class LogicalOperators_3 {
    public static void main(String[] args) {
//        !, Logical NOT: returns true when a condition is false and vice-versa
        boolean a = true;
        boolean b = false;
        System.out.println(!a); // o/p: false
        System.out.println(!b); // O/P: true

    }
}
